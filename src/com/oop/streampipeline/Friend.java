package com.oop.streampipeline;

public class Friend {
	String name;
	int age;
	
	public Friend(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String toString() {
		return name;
	}
}
